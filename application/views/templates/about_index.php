<script type="text/javascript">
$links = $('#hidden-links');
$link_menu_trigger = $('.link-menu-trigger').find('span');
$hide_status = true;

var show_offset = $('.nav').height() + 50;
$(function(){
	$(window).scroll(function(){
		if((window.scrollY >= show_offset && window.innerWidth > 800) || (window.scrollY >=   show_offset + 100 && window.innerWidth >= 500 && window.innerWidth <= 800) )
		{
			$link_menu_trigger.animate({
				opacity: 0.9,
				left : -20,
				width: 65
			}, 10);
			$link_menu_trigger.text('Menu');
			$link_menu_trigger.data('hidden', 0);
		}
		else if(window.scrollY < show_offset)
		{
			$link_menu_trigger.animate({
				left : -30,
				width: 0
			}, 10);
		}
		//if links menu is shown hide it right away
		$links.slideUp();
	});
});

$(function(){
	$link_menu_trigger.click(function(){
		if($(this).data('hidden') == 0)
		{
			var $article = $('#description');
			var articleContainerWidth = $article.innerWidth() + "px";
			var styles = 'width: ' + articleContainerWidth;
			if(window.innerWidth > 800 && window.innerWidth <= 890)
				styles += '; top : 100px' ;
			$links.attr('style', styles );

			//set data-hidden == false
			$(this).data('hidden', 1);

			//show link menus
			$links.slideDown();

			//change the text to Hide
			$(this).text('Hide');
		}
		else if($(this).data('hidden') == 1 )
		{
			$(this).data('hidden', 0);
			$links.slideUp();

			//change the text to Menu
			$(this).text('Menu');
		}
	});
});

</script>